fn main() {
    println!("Number: {}", 10);
    println!("{} is a {} day.", "Today", "good");
    println!("{2} is a {1} day.\nTomorrow would be {0}.", "better", "good", "Today");
    println!("{:?}", ("Bruh is my name", 101));
    println!("{} in binary = {:b}, octal = {:o} and in hexadecimal = {:x}", 10, 10, 10, 10);
}